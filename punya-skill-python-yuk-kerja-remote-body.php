													<p class="pa-content">Python merupakan bahasa pemrograman yang mudah dibaca dan dapat digunakan untuk berbagai macam proyek. Apa yang harus dilakukan setelah menguasai Python? Hal yang bisa kita lakukan salah satunya menambah pendapatan dengan kerja di Software House. Namun bila terhalang masalah geografis, karena jauh dari kota besar, kita bisa mencoba kerja remote.</p>
													<p class="pa-content">Tren kerja remote mulai menggeliat di Indonesia, namun untuk proyek Python masih jarang. Jika pembaca ingin mencoba kerja remote dengan orang luar negeri, kita bisa mencari peluangnya di situs-situs berikut:</p>
													<h2>Stack Overflow Careers</h2>
													<img class="img-responsive" src="assets/img/stackoverflow.png"/>
													<p class="pa-content">Keuntungan yang kita dapat dalam situs ini antara lain: 1) Jumlah proyek untuk Python cukup banyak, 2) Kualitas para pemberi kerja dengan proyek yang cukup rinci.</p>
													<p class="pa-content">Kunjungi situs resminya di <a class="red-link" href="#">http://careers.stackoverflow.com</a></p>
													<h2>FlexJobs</h2>
													<img class="img-responsive" src="assets/img/flexjobs.jpg"/>
													<p class="pa-content">Situs ini menawarkan berbagai proyek Python untuk semua posisi. Pasarnya juga bagus dan menggabungkan antara perusahaan dan startup.</p>
													<p class="pa-content">Kunjungi situs resminya di <a class="red-link" href="#">http://www.flexjobs.com</a></p>
													<h2>DjangoGigs</h2>
													<img class="img-responsive" src="assets/img/gigs.png"/>
													<p class="pa-content">Situs ini juga memberikan peluang proyek Python yang cukup besar. Apabila pembaca menguasai framework Django, ini menjadi poin yang bagus, karena DjangoGigs memang fokus untuk menyediakan pekerjaan bagi pengguna framework Django.</p>
													<p class="pa-content">Kunjungi situs resminya di <a class="red-link" href="#">http://www.djangogigs.com</a></p>
													<h2>Skip The Drive</h2>
													<img class="img-responsive" src="assets/img/skipthedrive.png"/>
													<p class="pa-content">Jika menggunakan situs ini kita tak perlu melakukan penyaringan proyek. Sedangkan untuk posisi yang disediakan kebanyakan perusahaan maupun agensi.</p>
													<p class="pa-content">Kunjungi situs resminya di <a class="red-link" href="#">http://www.skipthedrive.com</a></p>
													<h2>Simply Hired</h2>
													<img class="img-responsive" src="assets/img/simplyhired.png"/>
													<p class="pa-content">Simply Hired menyediakan berbagai proyek populer, untuk mencarinya gunakan top ranking. Banyak pilihan yang bisa digunakan untuk penyaringan lokasi maupun kata kunci proyek, diantaranya Remote, Telecommute atau Anywhere. Simply Hired juga menggabungkan perusahaan dan startup untuk posisi pekerjaan yang diinginkan.</p>
													<p class="pa-content">Kunjungi situs resminya di <a class="red-link" href="#">http://www.simplyhired.com</a></p>
													<h2>Linkedin</h2>
													<img class="img-responsive" src="assets/img/linkedin-post.png"/>
													<p class="pa-content">Situs yang paling dikenal bagi banyak orang. Untuk proyek Python, Linkedin juga menyediakan banyak peluang. Namun biasanya untuk perusahaan saja, dan opsi yang bisa gunakan untuk penyaringan adalah Telecommute.</p>
													<p class="pa-content">Kunjungi situs resminya di <a class="red-link" href="#">http://www.linkedin.com</a></p>
													<p class="pa-content">Proyek Python memang masih jarang di Indonesia, namun bukan berarti Python lemah. Sebenarnya proyek Python lebih banyak dicari di level internasional. Buktinya situs-situs tersebut masih menyediakan proyek Python yang melimpah. Nah jika pembaca seorang programmer Python, jangan khawatir peluang akan selalu ada. Selamat bekerja remote.</p>
